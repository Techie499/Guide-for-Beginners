class pythagoras
{
    public static void main(int a, int b, int c)
    {
        if (a*a+b*b==c*c||c*c+b*b==a*a||a*a+c*c==b*b)
        {
            System.out.println("It is a pythogoras triangle");
        }
        else
        {
            System.out.println("Not a pythogoras triangle");
        }
    }
}
          
       